public class elseif_ladder_ {

	public static void main(String[] args) {
		

		int c=12;
		
		if(c==15)
		{
			System.out.println("c is a 10");
		}
		else if (c==20)
		{
			System.out.println("c is a 20");
		}
			else if (c==30)
			{
				System.out.println("c is 30");
			}
			else 
			{
				System.out.println("c is invalid number");
			}
			}
	}


